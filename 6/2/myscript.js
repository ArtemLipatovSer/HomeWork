let number = parseInt(prompt("Введите число от 0 до 9:"))

switch (number){
    case 0:
        alert("0-)")
        break
    case 1:
        alert("1-!")
        break
    case 3:
        alert("3-#")
        break
    case 4:
        alert("4-$")
        break
    case 5:
        alert("5-%")
        break
    case 6:
        alert ("6-^")
        break
    case 7:
        alert ("7-&")
    case 8:
        alert ("8-*")
        break
    case 9:
        alert ("9-(")
        break
    default:
        alert("none")
}